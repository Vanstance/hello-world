    private void levelBFS(List<List<Integer>> list, TreeNode root, int level){
        if(root == null) return;
        if(level > list.size()){
            list.add(0, new ArrayList<Integer>());
        }
        levelBFS(list, root.left, level+1);
        levelBFS(list, root.right, level +1);
        
    }
